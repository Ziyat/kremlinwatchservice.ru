                <h4>Рекомендации и ограничения</h4>
                <p>
                    Не рекомендуется хранить часы в холодных помещениях. На холоде смазочные масла густеют, механизм
                    останавливается, цапфы осей могут быть повреждены.
                    Не следует оставлять часы на каменных столиках, например из мрамора, так как сильные перемены
                    температуры, которым подвержен мрамор, могут вызвать повреждения некоторых деталей, особенно пружин
                    и спиралей старых часов, которые делали из нержавеющей стали.
                </p>
                <p>
                    Рекомендуется снимать часы перед сном. Во время сна Вы производите неконтролируемые движения,
                    которые могут привести к повреждению часов. Кроме того, во сне человек потеет, и пот, постепенно
                    накапливаясь на Ваших часах, может повредить корпус и прокладки, обеспечивающие герметичность часов.
                    Если Ваши часы водостойкие, то Вы можете время от времени протирать их слабым раствором мягкого мыла
                    или чистящего вещества, после чего часы необходимо насухо вытирать. Если Ваши часы имеют кожаный
                    ремешок, то такая процедура должна проводиться только относительно корпуса часов. В случае, когда
                    Ваши часы не водонепроницаемые или Вы не уверены в этом, протирайте их слегка влажной тряпочкой,
                    после чего высушивайте сухой.
                </p>
                <p>
                    Если Вы регулярно занимаетесь спортом или ведете активный физический образ жизни, то Вам надо
                    подбирать себе часы на металлическом браслете или каучуковом ремешке, вместо натурального кожаного.
                    Пот и испарина, выделяющиеся при занятии спортом, будут постепенно разрушать натуральную кожу
                    браслета.
                </p>
