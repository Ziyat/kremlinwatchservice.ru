                    купания следует промыть мыльным раствором в теплой проточной воде, чтобы избежать коррозии и
                    преждевременного старения прокладок герметичности. Во время контакта с водой заводная коронка и все
                    имеющиеся кнопки должны быть во ввинченном положении.
